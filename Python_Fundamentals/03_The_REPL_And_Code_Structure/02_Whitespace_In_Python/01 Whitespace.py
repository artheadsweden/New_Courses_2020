# In Python whitespace matters

# Blocks of code, like here in the main function is indicated by indentation by four (4) spaces.
# Each new subblock adds another 4 spaces of indentation
def main():
    x = 3
    if x == 3:
        print("x is 3")
    else:
        print("x is something else")


if __name__ == '__main__':
    main()