def main():
    # Assignment
    x = 10
    print("Assignment (x = 10):", x)

    # Addition
    x = 10 + 3
    print("Addition (x = 10 + 3):", x)

    # Subtraction
    x = 10 - 3
    print("Subtraction (x = 10 - 3):", x)

    # Multiplication
    x = 10 * 3
    print("Multiplication (x = 10 * 3):", x)

    # Division
    x = 10 / 3
    print("Division (x = 10 / 3):", x)

    # Modulus
    x = 10 % 3
    print("Modulus (x = 10 % 3):", x)

    # Exponent
    x = 10 ** 3
    print("Exponent (x = 10 ** 3):", x)

    # Floor Division
    # the digits after the decimal point are removed
    x = 10 // 3
    print("Floor Division (x = 10 // 3):", x)
    # if one of the operands is negative, the result is floored, i.e., rounded away from zero
    x = 10 // -3
    print("Floor Division with negative operand(x = 10 // -3):", x)

    # Add and Assign
    x = 10
    x += 3
    print(x)

    # Subtract and Assign
    x = 10
    x -= 3
    print(x)

    # Multiply and Assign
    x = 10
    x *= 3
    print(x)

    # Divide and Assign
    x = 10
    x *= 3
    print(x)

    # Modulus and Assign
    x = 10
    x %= 3
    print(x)

    # Exponent and Assign
    x = 10
    x **= 3
    print(x)

    # Floor and Assign
    x = 10
    x //= 3
    print(x)




if __name__ == '__main__':
    main()