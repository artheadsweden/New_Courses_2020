def main():
    # Equal
    print(10 == 11)

    # Not Equal
    print(10 != 11)

    # Greater than
    print(10 > 11)

    # Less than
    print(10 < 11)

    # Greater or equal to
    print(10 >= 11)

    # Less or equal to
    print(10 <= 11)

    # Checking if something exist in a collection
    print('a' in 'face')

    # Testing for None
    value = None
    print(value is None)


if __name__ == '__main__':
    main()